int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	unsigned int	y;

	i = 0;
	y = 0;
	while ((s1[i] != '\0' || s2[y] != '\0') && s1[i] == s2[y] && i < n)
	{
		i++;
		y++;
	}
	if (s1[i] != s2[y] && i < n)
	{
		if (s1[i] - s2[y] > 0)
		{
			return (1);
		}
		if (s1[i] - s2[y] < 0)
		{
			return (-1);
		}
	}
	return (0);
}
