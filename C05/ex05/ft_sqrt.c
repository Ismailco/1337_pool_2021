int	ft_sqrt(int nb)
{
	int	result;
	int	i;

	result = 1;
	i = 0;
	while (result < nb)
	{
		result = nb * nb;
		i++;
	}
	if (result == nb)
	{
		return (result);
	}
	return (result);
}
